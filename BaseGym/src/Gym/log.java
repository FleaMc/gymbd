    private void bttnGoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnGoActionPerformed
       LocalDate fechaactual = LocalDate.now();
            try {
                pst = con.prepareStatement("SELECT * FROM Usuarios");
                ResultSet rs = pst.executeQuery();

                while(rs.next())
                {
                    String nom = rs.getString(1);
                    String us = rs.getString(2);

                    String pass = rs.getString(3);
                    String tip = rs.getString(4);

                    if(txtUser.getText().equals(us) && txtPass.getText().equals(pass)){
                        if(tip.equals("Administrador")){
                            log tipo = new log();
                            tipo.setNombre(nom);
                           
                            tipo.setTipo(1);
                            GymPOO gym = new GymPOO();
                            gym.setNom(nom);
                            gym.setVisible(true);
                            gym.setTipo(1);

                            gym.txtTip.setText("Nombre");
                            gym.vtipo.setText(nom);
                            gym.vfecha.setText(""+fechaactual);
                            gym.bttnNombre.setVisible(false);
                        }else{
                            log tipo = new log();
                            tipo.setNombre(nom);
                            GymPOO gym = new GymPOO();
                            gym.setNom(nom);
                            gym.setTipo(2);
                            gym.setVisible(true);                         
                            gym.bttnModificar.setVisible(false);
                            gym.bttnEliminar.setVisible(false);
                            gym.vtipo.setText(nom);
                            gym.vfecha.setText(""+fechaactual);
                        }

                    }else{
                                          
                    }

                }

            } catch (Exception ex) {
                
               
            }
        

    }//GEN-LAST:event_bttnGoActionPerformed

